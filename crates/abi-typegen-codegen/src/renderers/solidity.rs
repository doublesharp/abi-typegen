use crate::type_mapper::{safe_param_name, struct_name_from_internal_type};
use abi_typegen_core::types::{
    AbiError, AbiEvent, AbiEventParam, AbiFunction, ContractIr, NatSpec, SolType, StateMutability,
    TupleComponent,
};
use heck::ToUpperCamelCase;
use std::collections::HashSet;

#[derive(Debug, Clone)]
struct StructDef {
    name: String,
    components: Vec<TupleComponent>,
}

/// Returns the generated Solidity interface name for a contract.
pub fn interface_name(contract_name: &str) -> String {
    let mut chars = contract_name.chars();
    match (chars.next(), chars.next()) {
        (Some('I'), Some(second)) if second.is_ascii_uppercase() => contract_name.to_string(),
        _ => format!("I{}", contract_name),
    }
}

/// Generates a Solidity interface file for a parsed contract ABI.
pub fn render_solidity_file(ir: &ContractIr) -> String {
    let mut out = String::new();
    out.push_str("// Auto-generated by abi-typegen. Do not edit manually.\n");
    out.push_str("pragma solidity >=0.8.0;\n\n");
    if let Some(natspec) = &ir.natspec {
        out.push_str(&render_contract_docs(natspec));
    }
    out.push_str(&format!("interface {} {{\n", interface_name(&ir.name)));

    let mut has_items = false;
    for struct_def in collect_struct_defs(ir) {
        append_interface_item(&mut out, &mut has_items, &render_struct_def(&struct_def));
    }
    for error in &ir.errors {
        append_interface_item(&mut out, &mut has_items, &render_error(error));
    }
    for event in &ir.events {
        append_interface_item(&mut out, &mut has_items, &render_event(event));
    }
    for function in &ir.functions {
        append_interface_item(&mut out, &mut has_items, &render_function(function));
    }
    if ir.has_fallback {
        append_interface_item(&mut out, &mut has_items, "    fallback() external;");
    }
    if ir.has_receive {
        append_interface_item(&mut out, &mut has_items, "    receive() external payable;");
    }

    if has_items {
        out.push('\n');
    }
    out.push_str("}\n");
    out
}

fn append_interface_item(out: &mut String, has_items: &mut bool, item: &str) {
    if *has_items {
        out.push('\n');
        out.push('\n');
    }
    *has_items = true;
    out.push_str(item);
}

fn collect_struct_defs(ir: &ContractIr) -> Vec<StructDef> {
    let mut defs = Vec::new();
    let mut seen = HashSet::new();

    for function in &ir.functions {
        let function_name = function.name.to_upper_camel_case();
        for (index, input) in function.inputs.iter().enumerate() {
            collect_structs_in_type(
                &input.ty,
                input.internal_type.as_deref(),
                &format!(
                    "{}{}",
                    function_name,
                    tuple_name_segment(&input.name, index, "Param")
                ),
                &mut defs,
                &mut seen,
            );
        }
        for (index, output) in function.outputs.iter().enumerate() {
            collect_structs_in_type(
                &output.ty,
                output.internal_type.as_deref(),
                &format!("{}Result{}", function_name, index),
                &mut defs,
                &mut seen,
            );
        }
    }

    for event in &ir.events {
        let event_name = event.name.to_upper_camel_case();
        for (index, input) in event.inputs.iter().enumerate() {
            collect_structs_in_type(
                &input.ty,
                input.internal_type.as_deref(),
                &format!(
                    "{}Event{}",
                    event_name,
                    tuple_name_segment(&input.name, index, "Param")
                ),
                &mut defs,
                &mut seen,
            );
        }
    }

    for error in &ir.errors {
        let error_name = error.name.to_upper_camel_case();
        for (index, input) in error.inputs.iter().enumerate() {
            collect_structs_in_type(
                &input.ty,
                input.internal_type.as_deref(),
                &format!(
                    "{}Error{}",
                    error_name,
                    tuple_name_segment(&input.name, index, "Param")
                ),
                &mut defs,
                &mut seen,
            );
        }
    }

    defs
}

fn collect_structs_in_type(
    ty: &SolType,
    internal_type: Option<&str>,
    fallback_name: &str,
    defs: &mut Vec<StructDef>,
    seen: &mut HashSet<String>,
) {
    match ty {
        SolType::Tuple(components) => {
            let struct_name = tuple_type_name(internal_type, fallback_name);
            if seen.insert(struct_name.clone()) {
                for (index, component) in components.iter().enumerate() {
                    collect_structs_in_type(
                        &component.ty,
                        component.internal_type.as_deref(),
                        &format!(
                            "{}{}",
                            struct_name,
                            tuple_name_segment(&component.name, index, "Field")
                        ),
                        defs,
                        seen,
                    );
                }
                defs.push(StructDef {
                    name: struct_name,
                    components: components.clone(),
                });
            }
        }
        SolType::Array(inner) | SolType::FixedArray(inner, _) => {
            collect_structs_in_type(inner, internal_type, fallback_name, defs, seen);
        }
        SolType::Uint(_)
        | SolType::Int(_)
        | SolType::Bool
        | SolType::Address
        | SolType::Bytes
        | SolType::BytesN(_)
        | SolType::StringType => {}
    }
}

fn tuple_type_name(internal_type: Option<&str>, fallback_name: &str) -> String {
    internal_type
        .and_then(struct_name_from_internal_type)
        .unwrap_or(fallback_name)
        .to_upper_camel_case()
}

fn tuple_name_segment(name: &str, index: usize, fallback_prefix: &str) -> String {
    if name.is_empty() {
        format!("{}{}", fallback_prefix, index)
    } else {
        safe_param_name(name, index).to_upper_camel_case()
    }
}

fn render_struct_def(def: &StructDef) -> String {
    let mut out = String::new();
    out.push_str(&format!("    struct {} {{\n", def.name));
    for (index, component) in def.components.iter().enumerate() {
        out.push_str(&format!(
            "        {};\n",
            render_field_like(
                &component.ty,
                component.internal_type.as_deref(),
                &format!(
                    "{}{}",
                    def.name,
                    tuple_name_segment(&component.name, index, "Field")
                ),
                safe_param_name(&component.name, index),
                None,
            )
        ));
    }
    out.push_str("    }");
    out
}

fn render_error(error: &AbiError) -> String {
    let mut out = String::new();
    let param_names: Vec<&str> = error
        .inputs
        .iter()
        .map(|param| param.name.as_str())
        .collect();
    out.push_str(&indent_doc_block(&render_item_docs(
        error.natspec.as_ref(),
        &param_names,
        &[],
    )));
    out.push_str(&format!(
        "    error {}({});",
        error.name,
        render_error_params(error)
    ));
    out
}

fn render_event(event: &AbiEvent) -> String {
    let mut out = String::new();
    let param_names: Vec<&str> = event
        .inputs
        .iter()
        .map(|param| param.name.as_str())
        .collect();
    out.push_str(&indent_doc_block(&render_item_docs(
        event.natspec.as_ref(),
        &param_names,
        &[],
    )));
    out.push_str(&format!(
        "    event {}({}){};",
        event.name,
        render_event_params(event),
        if event.anonymous { " anonymous" } else { "" }
    ));
    out
}

fn render_function(function: &AbiFunction) -> String {
    let mut out = String::new();
    let param_names: Vec<&str> = function
        .inputs
        .iter()
        .map(|param| param.name.as_str())
        .collect();
    let return_names: Vec<&str> = function
        .outputs
        .iter()
        .map(|param| param.name.as_str())
        .collect();
    out.push_str(&indent_doc_block(&render_item_docs(
        function.natspec.as_ref(),
        &param_names,
        &return_names,
    )));

    out.push_str(&format!(
        "    function {}({}) external{}",
        function.name,
        render_function_params(function),
        render_mutability(&function.state_mutability)
    ));
    if !function.outputs.is_empty() {
        out.push_str(&format!(" returns ({})", render_function_outputs(function)));
    }
    out.push(';');
    out
}

fn render_function_params(function: &AbiFunction) -> String {
    function
        .inputs
        .iter()
        .enumerate()
        .map(|(index, input)| {
            render_field_like(
                &input.ty,
                input.internal_type.as_deref(),
                &format!(
                    "{}{}",
                    function.name.to_upper_camel_case(),
                    tuple_name_segment(&input.name, index, "Param")
                ),
                safe_param_name(&input.name, index),
                data_location_for_type(&input.ty, true),
            )
        })
        .collect::<Vec<_>>()
        .join(", ")
}

fn render_function_outputs(function: &AbiFunction) -> String {
    let mut used_names = function
        .inputs
        .iter()
        .enumerate()
        .map(|(index, input)| safe_param_name(&input.name, index))
        .collect::<HashSet<_>>();

    function
        .outputs
        .iter()
        .enumerate()
        .map(|(index, output)| {
            let output_name = if output.name.is_empty() {
                None
            } else {
                let candidate = safe_param_name(&output.name, index);
                if used_names.insert(candidate.clone()) {
                    Some(candidate)
                } else {
                    None
                }
            };

            render_type_with_optional_name(
                &output.ty,
                output.internal_type.as_deref(),
                &format!("{}Result{}", function.name.to_upper_camel_case(), index),
                output_name,
                data_location_for_type(&output.ty, false),
            )
        })
        .collect::<Vec<_>>()
        .join(", ")
}

fn render_event_params(event: &AbiEvent) -> String {
    event
        .inputs
        .iter()
        .enumerate()
        .map(|(index, input)| render_event_param(input, &event.name, index))
        .collect::<Vec<_>>()
        .join(", ")
}

fn render_event_param(input: &AbiEventParam, event_name: &str, index: usize) -> String {
    let name = safe_param_name(&input.name, index);
    let ty = render_solidity_type(
        &input.ty,
        input.internal_type.as_deref(),
        &format!(
            "{}Event{}",
            event_name.to_upper_camel_case(),
            tuple_name_segment(&input.name, index, "Param")
        ),
    );
    if input.indexed {
        format!("{} indexed {}", ty, name)
    } else {
        format!("{} {}", ty, name)
    }
}

fn render_error_params(error: &AbiError) -> String {
    error
        .inputs
        .iter()
        .enumerate()
        .map(|(index, input)| {
            render_field_like(
                &input.ty,
                input.internal_type.as_deref(),
                &format!(
                    "{}Error{}",
                    error.name.to_upper_camel_case(),
                    tuple_name_segment(&input.name, index, "Param")
                ),
                safe_param_name(&input.name, index),
                None,
            )
        })
        .collect::<Vec<_>>()
        .join(", ")
}

fn render_field_like(
    ty: &SolType,
    internal_type: Option<&str>,
    fallback_name: &str,
    name: String,
    data_location: Option<&'static str>,
) -> String {
    let mut out = render_solidity_type(ty, internal_type, fallback_name);
    if let Some(location) = data_location {
        out.push(' ');
        out.push_str(location);
    }
    out.push(' ');
    out.push_str(&name);
    out
}

fn render_type_with_optional_name(
    ty: &SolType,
    internal_type: Option<&str>,
    fallback_name: &str,
    name: Option<String>,
    data_location: Option<&'static str>,
) -> String {
    let mut out = render_solidity_type(ty, internal_type, fallback_name);
    if let Some(location) = data_location {
        out.push(' ');
        out.push_str(location);
    }
    if let Some(name) = name {
        out.push(' ');
        out.push_str(&name);
    }
    out
}

fn render_solidity_type(ty: &SolType, internal_type: Option<&str>, fallback_name: &str) -> String {
    match ty {
        SolType::Uint(bits) => format!("uint{}", bits),
        SolType::Int(bits) => format!("int{}", bits),
        SolType::Bool => "bool".to_string(),
        SolType::Address => "address".to_string(),
        SolType::Bytes => "bytes".to_string(),
        SolType::BytesN(size) => format!("bytes{}", size),
        SolType::StringType => "string".to_string(),
        SolType::Array(inner) => {
            format!(
                "{}[]",
                render_solidity_type(inner, internal_type, fallback_name)
            )
        }
        SolType::FixedArray(inner, size) => {
            format!(
                "{}[{}]",
                render_solidity_type(inner, internal_type, fallback_name),
                size
            )
        }
        SolType::Tuple(_) => tuple_type_name(internal_type, fallback_name),
    }
}

fn data_location_for_type(ty: &SolType, is_input: bool) -> Option<&'static str> {
    match ty {
        SolType::StringType
        | SolType::Bytes
        | SolType::Array(_)
        | SolType::FixedArray(_, _)
        | SolType::Tuple(_) => Some(if is_input { "calldata" } else { "memory" }),
        SolType::Uint(_)
        | SolType::Int(_)
        | SolType::Bool
        | SolType::Address
        | SolType::BytesN(_) => None,
    }
}

fn render_mutability(state_mutability: &StateMutability) -> &'static str {
    match state_mutability {
        StateMutability::Pure => " pure",
        StateMutability::View => " view",
        StateMutability::NonPayable => "",
        StateMutability::Payable => " payable",
    }
}

fn render_contract_docs(natspec: &NatSpec) -> String {
    render_item_docs(Some(natspec), &[], &[])
}

fn indent_doc_block(block: &str) -> String {
    if block.is_empty() {
        return String::new();
    }

    block
        .lines()
        .map(|line| format!("    {}", line))
        .collect::<Vec<_>>()
        .join("\n")
        + "\n"
}

fn render_item_docs(natspec: Option<&NatSpec>, params: &[&str], returns: &[&str]) -> String {
    let Some(natspec) = natspec else {
        return String::new();
    };

    let mut lines = Vec::new();
    if let Some(notice) = &natspec.notice {
        lines.push(format!("/// @notice {}", notice));
    }
    if let Some(dev) = &natspec.dev {
        lines.push(format!("/// @dev {}", dev));
    }
    for param in params {
        if param.is_empty() {
            continue;
        }
        if let Some(description) = natspec.params.get(*param) {
            lines.push(format!("/// @param {} {}", param, description));
        }
    }
    for ret in returns {
        if ret.is_empty() {
            continue;
        }
        if let Some(description) = natspec.returns.get(*ret) {
            lines.push(format!("/// @return {} {}", ret, description));
        }
    }

    if lines.is_empty() {
        String::new()
    } else {
        format!("{}\n", lines.join("\n"))
    }
}

#[cfg(test)]
mod tests {
    use super::*;
    use abi_typegen_core::parser::parse_artifact;

    fn fixture(contract_name: &str, file_name: &str) -> ContractIr {
        let json = std::fs::read_to_string(
            std::path::Path::new(env!("CARGO_MANIFEST_DIR"))
                .join("../../tests/fixtures")
                .join(file_name),
        )
        .unwrap();
        parse_artifact(contract_name, &json).unwrap()
    }

    #[test]
    fn prefixes_interface_name_once() {
        assert_eq!(interface_name("Vault"), "IVault");
        assert_eq!(interface_name("IERC20"), "IERC20");
    }

    #[test]
    fn contains_header_and_interface() {
        let out = render_solidity_file(&fixture("Token", "erc20.json"));
        assert!(out.contains("// Auto-generated by abi-typegen."));
        assert!(out.contains("pragma solidity >=0.8.0;"));
        assert!(out.contains("interface IToken {"));
    }

    #[test]
    fn renders_functions_events_and_errors() {
        let out = render_solidity_file(&fixture("Token", "erc20.json"));
        assert!(out
            .contains("event Transfer(address indexed from, address indexed to, uint256 value);"));
        assert!(out.contains(
            "error ERC20InsufficientBalance(address sender, uint256 balance, uint256 needed);"
        ));
        assert!(
            out.contains("function balanceOf(address account) external view returns (uint256);")
        );
        assert!(
            out.contains("function transfer(address to, uint256 amount) external returns (bool);")
        );
    }

    #[test]
    fn renders_named_structs_for_tuple_outputs() {
        let out = render_solidity_file(&fixture("Vault", "vault.json"));
        assert!(out.contains("struct Position {"));
        assert!(out.contains("uint256 shares;"));
        assert!(out.contains(
            "function getPosition(address user) external view returns (Position memory position);"
        ));
    }

    #[test]
    fn renders_overloads_and_receive() {
        let out = render_solidity_file(&fixture("Vault", "vault.json"));
        assert!(out.contains("error InsufficientShares(uint256 requested, uint256 available);"));
        assert!(
            out.contains("event Deposited(address indexed user, uint256 amount, uint256 shares);")
        );
        assert!(out.contains("function deposit(uint256 amount) external;"));
        assert!(out.contains("function deposit(uint256 amount, address recipient) external;"));
        assert!(out.contains("receive() external payable;"));
    }

    #[test]
    fn renders_fallback_and_receive() {
        let json = r#"{
            "abi": [
                { "type": "fallback", "stateMutability": "nonpayable" },
                { "type": "receive", "stateMutability": "payable" }
            ]
        }"#;
        let ir = parse_artifact("Receiver", json).unwrap();
        let out = render_solidity_file(&ir);
        assert!(out.contains("fallback() external;"));
        assert!(out.contains("receive() external payable;"));
    }

    #[test]
    fn indents_item_docs_and_closes_interface_cleanly() {
        let json = r#"{
            "abi": [
                {
                    "type": "function",
                    "name": "multiReturn",
                    "inputs": [],
                    "outputs": [
                        {"name": "count", "type": "uint256", "internalType": "uint256", "components": []},
                        {"name": "total", "type": "uint256", "internalType": "uint256", "components": []},
                        {"name": "flag", "type": "bool", "internalType": "bool", "components": []}
                    ],
                    "stateMutability": "pure"
                }
            ],
            "metadata": {
                "output": {
                    "devdoc": {
                        "kind": "dev",
                        "version": 1,
                        "methods": {
                            "multiReturn()": {
                                "returns": {
                                    "count": "The count",
                                    "total": "The total",
                                    "flag": "A flag"
                                }
                            }
                        }
                    },
                    "userdoc": {
                        "kind": "user",
                        "version": 1,
                        "methods": {
                            "multiReturn()": {
                                "notice": "Returns multiple named values"
                            }
                        }
                    }
                }
            }
        }"#;

        let ir = parse_artifact("EdgeCases", json).unwrap();
        let out = render_solidity_file(&ir);

        assert!(out.contains(
            "    /// @notice Returns multiple named values\n    /// @return count The count\n    /// @return total The total\n    /// @return flag A flag\n    function multiReturn() external pure returns (uint256 count, uint256 total, bool flag);"
        ));
        assert!(out.ends_with(
            "    function multiReturn() external pure returns (uint256 count, uint256 total, bool flag);\n}\n"
        ));
    }
}
