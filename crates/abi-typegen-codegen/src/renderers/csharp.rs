use abi_typegen_core::types::{ContractIr, SolType, TupleComponent};
use heck::ToUpperCamelCase;
use std::collections::HashMap;

/// Generates `<ContractName>.cs` — a C# source file with typed classes
/// compatible with Nethereum's `[Parameter]` and `[Event]` attributes.
pub fn render_csharp_file(ir: &ContractIr) -> String {
    let mut out = String::new();

    // Header
    out.push_str("// Auto-generated by abi-typegen. Do not edit manually.\n\n");

    // Conditional imports
    let needs_big = ir_uses_big_integer(ir);
    if needs_big {
        out.push_str("using System.Numerics;\n");
    }
    out.push_str("using System.Collections.Generic;\n");
    out.push_str("using Nethereum.ABI.FunctionEncoding.Attributes;\n");

    out.push_str("\nnamespace Contracts\n{\n");

    let mut first_class = true;

    // Count function name occurrences for overload detection
    let mut name_counts: HashMap<&str, u32> = HashMap::new();
    for f in &ir.functions {
        *name_counts.entry(f.name.as_str()).or_insert(0) += 1;
    }

    // Function param classes
    for f in &ir.functions {
        if f.inputs.is_empty() {
            continue;
        }
        if !first_class {
            out.push('\n');
        }
        first_class = false;

        let count = *name_counts.get(f.name.as_str()).unwrap_or(&1);
        let suffix = if count > 1 {
            overload_suffix(&f.inputs)
        } else {
            String::new()
        };
        let class_name = format!(
            "{}{}{}Params",
            ir.name,
            f.name.to_upper_camel_case(),
            suffix
        );

        // Doc comment
        if let Some(ref natspec) = f.natspec {
            if let Some(ref notice) = natspec.notice {
                out.push_str(&format!(
                    "    /// <summary>Parameters for the {} function. {}</summary>\n",
                    f.name, notice
                ));
            } else {
                out.push_str(&format!(
                    "    /// <summary>Parameters for the {} function.</summary>\n",
                    f.name
                ));
            }
        } else {
            out.push_str(&format!(
                "    /// <summary>Parameters for the {} function.</summary>\n",
                f.name
            ));
        }

        out.push_str(&format!("    public class {}\n    {{\n", class_name));
        for (i, param) in f.inputs.iter().enumerate() {
            if i > 0 {
                out.push('\n');
            }
            let sol_name = safe_param_name(&param.name, i);
            let field_name = sol_name.to_upper_camel_case();
            let cs_type = sol_type_to_csharp(&param.ty);
            let sol_type_str = sol_type_to_sol_string(&param.ty);
            let order = i + 1;
            out.push_str(&format!(
                "        [Parameter(\"{}\", \"{}\", {})]\n",
                sol_type_str, sol_name, order
            ));
            out.push_str(&format!(
                "        public {} {} {{ get; set; }}\n",
                cs_type, field_name
            ));
        }
        out.push_str("    }\n");
    }

    // Event classes
    for event in &ir.events {
        if !first_class {
            out.push('\n');
        }
        first_class = false;

        let class_name = format!("{}{}Event", ir.name, event.name);

        // Doc comment
        if let Some(ref natspec) = event.natspec {
            if let Some(ref notice) = natspec.notice {
                out.push_str(&format!(
                    "    /// <summary>The {} event. {}</summary>\n",
                    event.name, notice
                ));
            } else {
                out.push_str(&format!(
                    "    /// <summary>The {} event.</summary>\n",
                    event.name
                ));
            }
        } else {
            out.push_str(&format!(
                "    /// <summary>The {} event.</summary>\n",
                event.name
            ));
        }

        out.push_str(&format!(
            "    [Event(\"{}\")]\n    public class {}\n    {{\n",
            event.name, class_name
        ));
        for (i, param) in event.inputs.iter().enumerate() {
            if i > 0 {
                out.push('\n');
            }
            let sol_name = safe_param_name(&param.name, i);
            let field_name = sol_name.to_upper_camel_case();
            let cs_type = sol_type_to_csharp(&param.ty);
            let sol_type_str = sol_type_to_sol_string(&param.ty);
            let order = i + 1;
            out.push_str(&format!(
                "        [Parameter(\"{}\", \"{}\", {}, {})]\n",
                sol_type_str,
                sol_name,
                order,
                if param.indexed { "true" } else { "false" }
            ));
            out.push_str(&format!(
                "        public {} {} {{ get; set; }}\n",
                cs_type, field_name
            ));
        }
        out.push_str("    }\n");
    }

    // Error classes
    for error in &ir.errors {
        if error.inputs.is_empty() {
            continue;
        }
        if !first_class {
            out.push('\n');
        }
        first_class = false;

        let class_name = format!("{}{}Error", ir.name, error.name);

        // Doc comment
        if let Some(ref natspec) = error.natspec {
            if let Some(ref notice) = natspec.notice {
                out.push_str(&format!(
                    "    /// <summary>The {} error. {}</summary>\n",
                    error.name, notice
                ));
            } else {
                out.push_str(&format!(
                    "    /// <summary>The {} error.</summary>\n",
                    error.name
                ));
            }
        } else {
            out.push_str(&format!(
                "    /// <summary>The {} error.</summary>\n",
                error.name
            ));
        }

        out.push_str(&format!("    public class {}\n    {{\n", class_name));
        for (i, param) in error.inputs.iter().enumerate() {
            if i > 0 {
                out.push('\n');
            }
            let sol_name = safe_param_name(&param.name, i);
            let field_name = sol_name.to_upper_camel_case();
            let cs_type = sol_type_to_csharp(&param.ty);
            let sol_type_str = sol_type_to_sol_string(&param.ty);
            let order = i + 1;
            out.push_str(&format!(
                "        [Parameter(\"{}\", \"{}\", {})]\n",
                sol_type_str, sol_name, order
            ));
            out.push_str(&format!(
                "        public {} {} {{ get; set; }}\n",
                cs_type, field_name
            ));
        }
        out.push_str("    }\n");
    }

    out.push_str("}\n");

    out
}

/// Converts a [`SolType`] to its C# type representation.
fn sol_type_to_csharp(ty: &SolType) -> String {
    match ty {
        SolType::Bool => "bool".into(),
        SolType::StringType => "string".into(),
        SolType::Address => "string".into(),
        SolType::Bytes => "byte[]".into(),
        SolType::BytesN(_) => "byte[]".into(),

        SolType::Uint(bits) => match *bits {
            0..=8 => "byte".into(),
            9..=16 => "ushort".into(),
            17..=32 => "uint".into(),
            33..=64 => "ulong".into(),
            _ => "BigInteger".into(),
        },

        SolType::Int(bits) => match *bits {
            0..=8 => "sbyte".into(),
            9..=16 => "short".into(),
            17..=32 => "int".into(),
            33..=64 => "long".into(),
            _ => "BigInteger".into(),
        },

        SolType::Array(inner) | SolType::FixedArray(inner, _) => {
            let inner_cs = sol_type_to_csharp(inner);
            format!("List<{}>", inner_cs)
        }

        SolType::Tuple(components) => render_inline_class(components),
    }
}

/// Converts a [`SolType`] back to its Solidity type string for use in
/// Nethereum `[Parameter]` attributes.
fn sol_type_to_sol_string(ty: &SolType) -> String {
    match ty {
        SolType::Bool => "bool".into(),
        SolType::StringType => "string".into(),
        SolType::Address => "address".into(),
        SolType::Bytes => "bytes".into(),
        SolType::BytesN(n) => format!("bytes{}", n),
        SolType::Uint(bits) => format!("uint{}", bits),
        SolType::Int(bits) => format!("int{}", bits),
        SolType::Array(inner) => {
            let inner_sol = sol_type_to_sol_string(inner);
            format!("{}[]", inner_sol)
        }
        SolType::FixedArray(inner, n) => {
            let inner_sol = sol_type_to_sol_string(inner);
            format!("{}[{}]", inner_sol, n)
        }
        SolType::Tuple(components) => {
            let fields: Vec<String> = components
                .iter()
                .map(|c| sol_type_to_sol_string(&c.ty))
                .collect();
            format!("({})", fields.join(","))
        }
    }
}

/// Renders an inline C# class name for tuple types.
///
/// For Nethereum compatibility, tuples map to `object` since inline class
/// definitions are not supported in C# the same way as Go structs.
fn render_inline_class(components: &[TupleComponent]) -> String {
    if components.is_empty() {
        return "object".into();
    }
    // In practice, tuples in Nethereum are represented as separate classes.
    // For inline usage we fall back to `object`.
    "object".into()
}

/// Returns `true` if any type in the contract IR maps to `BigInteger`.
fn ir_uses_big_integer(ir: &ContractIr) -> bool {
    let check_params = |params: &[abi_typegen_core::types::AbiParam]| -> bool {
        params.iter().any(|p| type_uses_big_integer(&p.ty))
    };
    let check_event_params = |params: &[abi_typegen_core::types::AbiEventParam]| -> bool {
        params.iter().any(|p| type_uses_big_integer(&p.ty))
    };

    ir.functions
        .iter()
        .any(|f| check_params(&f.inputs) || check_params(&f.outputs))
        || ir.events.iter().any(|e| check_event_params(&e.inputs))
        || ir.errors.iter().any(|e| check_params(&e.inputs))
}

/// Returns `true` if the given Solidity type maps to `BigInteger` in C#.
fn type_uses_big_integer(ty: &SolType) -> bool {
    match ty {
        SolType::Uint(bits) | SolType::Int(bits) => *bits > 64,
        SolType::Array(inner) | SolType::FixedArray(inner, _) => type_uses_big_integer(inner),
        SolType::Tuple(components) => components.iter().any(|c| type_uses_big_integer(&c.ty)),
        _ => false,
    }
}

