/// Generates `<ContractName>.py` -- a typed Python stub for web3.py.
pub fn render_python_file(ir: &ContractIr) -> String {
    let mut out = String::new();
    let name = &ir.name;
    let name_upper = name.to_ascii_uppercase();

    // Header
    out.push_str("# Auto-generated by abi-typegen. Do not edit manually.\n");
    out.push_str(&format!(
        "\"\"\"Type stubs for the {} contract.\"\"\"\n\n",
        name
    ));

    // Imports
    out.push_str("from __future__ import annotations\n\n");
    out.push_str("import json\n");
    out.push_str("from typing import Any, TypedDict\n\n");
    out.push_str("from eth_typing import ChecksumAddress\n");
    out.push_str("from web3 import Web3\n");
    out.push_str("from web3.contract import Contract\n\n\n");

    // Embedded ABI
    let abi_json = serde_json::to_string_pretty(&ir.raw_abi).unwrap_or_else(|_| "[]".to_string());
    out.push_str(&format!(
        "{}_ABI: list[dict[str, Any]] = json.loads('''\n{}\n''')\n\n\n",
        name_upper, abi_json
    ));

    // Collect tuple types that should be emitted as TypedDict classes.
    let mut typed_dicts: Vec<(String, Vec<(String, String)>)> = Vec::new();
    collect_typed_dicts(ir, &mut typed_dicts);

    for (class_name, fields) in &typed_dicts {
        out.push_str(&format!("class {}(TypedDict):\n", class_name));
        if fields.is_empty() {
            out.push_str("    pass\n");
        } else {
            for (field_name, field_type) in fields {
                out.push_str(&format!("    {}: {}\n", field_name, field_type));
            }
        }
        out.push_str("\n\n");
    }

    // Contract wrapper class
    out.push_str(&format!("class {}Contract:\n", name));

    // Docstring from NatSpec or default
    if let Some(ref ns) = ir.natspec {
        if let Some(ref notice) = ns.notice {
            out.push_str(&format!(
                "    \"\"\"{}.\"\"\"\n",
                notice.trim_end_matches('.')
            ));
        } else {
            out.push_str(&format!(
                "    \"\"\"Typed wrapper for the {} contract.\"\"\"\n",
                name
            ));
        }
    } else {
        out.push_str(&format!(
            "    \"\"\"Typed wrapper for the {} contract.\"\"\"\n",
            name
        ));
    }
    out.push('\n');

    // __init__
    out.push_str("    def __init__(self, address: ChecksumAddress, w3: Web3) -> None:\n");
    out.push_str(&format!(
        "        self.contract: Contract = w3.eth.contract(\n\
         {s}address=address, abi={name_upper}_ABI,\n\
         {s2})\n",
        s = "            ",
        s2 = "        ",
    ));

    // Partition functions into view/pure and write
    let mut view_fns: Vec<&AbiFunction> = Vec::new();
    let mut write_fns: Vec<&AbiFunction> = Vec::new();
    for f in &ir.functions {
        match f.state_mutability {
            StateMutability::Pure | StateMutability::View => view_fns.push(f),
            _ => write_fns.push(f),
        }
    }

    if !view_fns.is_empty() {
        // Count overloaded names within the view partition
        let mut view_name_counts: HashMap<&str, u32> = HashMap::new();
        for f in &view_fns {
            *view_name_counts.entry(f.name.as_str()).or_insert(0) += 1;
        }

        out.push('\n');
        out.push_str("    # View/pure functions\n");
        for f in &view_fns {
            let method_name = resolve_method_name(f, &view_name_counts);
            out.push_str(&render_view_function(&method_name, f));
        }
    }

    if !write_fns.is_empty() {
        // Count overloaded names within the write partition
        let mut write_name_counts: HashMap<&str, u32> = HashMap::new();
        for f in &write_fns {
            *write_name_counts.entry(f.name.as_str()).or_insert(0) += 1;
        }

        out.push('\n');
        out.push_str("    # Write functions\n");
        for f in &write_fns {
            let method_name = resolve_method_name(f, &write_name_counts);
            out.push_str(&render_write_function(&method_name, f));
        }
    }

    out
}

/// Resolves the Python method name, converting to snake_case and appending
/// a type-based suffix for overloaded functions.
fn resolve_method_name(f: &AbiFunction, name_counts: &HashMap<&str, u32>) -> String {
    let count = *name_counts.get(f.name.as_str()).unwrap_or(&1);
    let snake = f.name.to_snake_case();
    if count > 1 {
        let suffix = overload_suffix(&f.inputs).to_snake_case();
        if suffix.is_empty() {
            snake
        } else {
            format!("{}_{}", snake, suffix)
        }
    } else {
        snake
    }
}

/// Converts a Solidity type to its Python type string.
fn sol_type_to_python(ty: &SolType) -> String {
    match ty {
        SolType::Uint(_) | SolType::Int(_) => "int".into(),
        SolType::Bool => "bool".into(),
        SolType::Address => "ChecksumAddress".into(),
        SolType::Bytes | SolType::BytesN(_) => "bytes".into(),
        SolType::StringType => "str".into(),
        SolType::Array(inner) | SolType::FixedArray(inner, _) => {
            format!("list[{}]", sol_type_to_python(inner))
        }
        SolType::Tuple(_) => "dict[str, Any]".into(),
    }
}

/// Python keywords that cannot be used as parameter names.
///
/// This supplements the JS/TS reserved-word check in `safe_param_name` with
/// words that are legal identifiers in JavaScript but keywords in Python
/// (e.g. `from`, `as`, `lambda`, `nonlocal`).
const PYTHON_KEYWORDS: &[&str] = &[
    "False", "None", "True", "and", "as", "assert", "async", "await", "break", "class", "continue",
    "def", "del", "elif", "else", "except", "finally", "for", "from", "global", "if", "import",
    "in", "is", "lambda", "nonlocal", "not", "or", "pass", "raise", "return", "try", "while",
    "with", "yield",
];

/// Converts a parameter name to a Python-safe snake_case name.
///
/// Checks both JS/TS reserved words (via `safe_param_name`) and Python
/// keywords, since names like `from` are valid in TypeScript but not Python.
/// The Python keyword check runs after snake_case conversion so that
/// camelCase names like `From` → `from` are also caught.
fn python_safe_param_name(name: &str, index: usize) -> String {
    let base = safe_param_name(name, index);
    let snake = base.to_snake_case();
    if PYTHON_KEYWORDS.contains(&snake.as_str()) {
        format!("_{}", snake)
    } else {
        snake
    }
}

/// Builds the Python return type for a view/pure function.
fn python_return_type(f: &AbiFunction) -> String {
    let out_types: Vec<String> = f
        .outputs
        .iter()
        .map(|p| sol_type_to_python(&p.ty))
        .collect();
    match out_types.len() {
        0 => "None".into(),
        1 => out_types[0].clone(),
        _ => format!("tuple[{}]", out_types.join(", ")),
    }
}

/// Renders a view/pure function as a Python stub method.
fn render_view_function(method_name: &str, f: &AbiFunction) -> String {
    let params = build_param_list(f);
    let ret = python_return_type(f);

    let mut s = String::new();
    if let Some(doc) = natspec_docstring(f) {
        s.push_str(&format!(
            "    def {}(self{}) -> {}:\n        \"\"\"{}\"\"\"\n        ...\n",
            method_name, params, ret, doc
        ));
    } else {
        s.push_str(&format!(
            "    def {}(self{}) -> {}: ...\n",
            method_name, params, ret
        ));
    }
    s
}

/// Renders a write (nonpayable/payable) function as a Python stub method.
fn render_write_function(method_name: &str, f: &AbiFunction) -> String {
    let params = build_param_list(f);

    let mut s = String::new();
    if let Some(doc) = natspec_docstring(f) {
        s.push_str(&format!(
            "    def {}(self{}) -> dict[str, Any]:\n        \"\"\"{}\"\"\"\n        ...\n",
            method_name, params, doc
        ));
    } else {
        s.push_str(&format!(
            "    def {}(self{}) -> dict[str, Any]: ...\n",
            method_name, params
        ));
    }
    s
}

/// Builds the parameter list string (without `self`) for a function.
fn build_param_list(f: &AbiFunction) -> String {
    if f.inputs.is_empty() {
        return String::new();
    }
    let params: Vec<String> = f
        .inputs
        .iter()
        .enumerate()
        .map(|(i, p)| {
            let pname = python_safe_param_name(&p.name, i);
            let pty = sol_type_to_python(&p.ty);
            format!("{}: {}", pname, pty)
        })
        .collect();
    format!(", {}", params.join(", "))
}

/// Extracts a NatSpec docstring from a function, if available.
fn natspec_docstring(f: &AbiFunction) -> Option<String> {
    let ns = f.natspec.as_ref()?;
    ns.notice.as_ref().map(|s| s.trim().to_string())
}

/// Collects all tuple types that should be emitted as TypedDict classes.
fn collect_typed_dicts(ir: &ContractIr, dicts: &mut Vec<(String, Vec<(String, String)>)>) {
    let mut seen = HashSet::new();

    // Collect from function inputs/outputs
    for f in &ir.functions {
        for p in f.inputs.iter().chain(f.outputs.iter()) {
            if let SolType::Tuple(ref components) = p.ty {
                let struct_name = p
                    .internal_type
                    .as_deref()
                    .and_then(struct_name_from_internal_type)
                    .unwrap_or(&p.name);
                if struct_name.is_empty() {
                    continue;
                }
                let class_name = format!("{}{}", ir.name, struct_name);
                if seen.insert(class_name.clone()) {
                    let fields = typed_dict_fields(components);
                    dicts.push((class_name, fields));
                }
            }
        }
    }

    // Collect from event parameters
    for e in &ir.events {
        for p in &e.inputs {
            if let SolType::Tuple(ref components) = p.ty {
                let struct_name = p
                    .internal_type
                    .as_deref()
                    .and_then(struct_name_from_internal_type)
                    .unwrap_or(&p.name);
                if struct_name.is_empty() {
                    continue;
                }
                let class_name = format!("{}{}", ir.name, struct_name);
                if seen.insert(class_name.clone()) {
                    let fields = typed_dict_fields(components);
                    dicts.push((class_name, fields));
                }
            }
        }
    }

    // Collect from error parameters
    for e in &ir.errors {
        for p in &e.inputs {
            if let SolType::Tuple(ref components) = p.ty {
                let struct_name = p
                    .internal_type
                    .as_deref()
                    .and_then(struct_name_from_internal_type)
                    .unwrap_or(&p.name);
                if struct_name.is_empty() {
                    continue;
                }
                let class_name = format!("{}{}", ir.name, struct_name);
                if seen.insert(class_name.clone()) {
                    let fields = typed_dict_fields(components);
                    dicts.push((class_name, fields));
                }
            }
        }
    }
}

/// Converts tuple components to TypedDict field definitions.
fn typed_dict_fields(components: &[TupleComponent]) -> Vec<(String, String)> {
    components
        .iter()
        .enumerate()
        .map(|(i, c)| {
            let fname = python_safe_param_name(&c.name, i);
            let ftype = sol_type_to_python(&c.ty);
            (fname, ftype)
        })
        .collect()
}

