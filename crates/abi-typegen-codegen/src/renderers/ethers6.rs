    #[test]
    fn contract_with_no_functions() {
        let ir = make_ir(
            "EventOnly",
            vec![],
            vec![AbiEvent {
                name: "Ping".to_string(),
                inputs: vec![AbiEventParam {
                    name: "sender".to_string(),
                    ty: SolType::Address,
                    indexed: true,
                    internal_type: None,
                }],
                anonymous: false,
                natspec: None,
            }],
        );
        let out = render_ethers_file(&ir);
        // The interface should still contain the filters block
        assert!(
            out.contains("filters: {"),
            "Expected `filters` block even with no functions, got:\n{out}"
        );
        assert!(
            out.contains("Ping(sender?: string | null): EventFilter;"),
            "Expected Ping filter, got:\n{out}"
        );
        // There should be no function signatures (lines starting with two spaces
        // that look like function declarations, but filters block is fine).
        // Specifically, no line matching `  someName(` outside the filters block.
        let interface_start = out.find("Contract {").expect("interface");
        let filters_start = out.find("filters: {").expect("filters");
        let between = &out[interface_start..filters_start];
        // Between the opening brace and filters there should only be whitespace/newlines
        let trimmed = between
            .trim_start_matches(|c: char| c != '{')
            .trim_start_matches('{')
            .trim();
        assert!(
            trimmed.is_empty(),
            "Expected no function signatures before filters block, got:\n{between}"
        );
    }

