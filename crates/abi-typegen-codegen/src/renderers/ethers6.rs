use crate::type_mapper::{Target, overload_suffix, safe_param_name, sol_type_to_ts};
use abi_typegen_core::types::{AbiEvent, AbiFunction, ContractIr, StateMutability};
use std::collections::HashMap;

const TARGET: Target = Target::Ethers;

/// Generates `<ContractName>.ethers.ts` — a typed ethers v6 contract interface.
pub fn render_ethers_file(ir: &ContractIr) -> String {
    let mut out = String::new();

    out.push_str("// Auto-generated by abi-typegen. Do not edit manually.\n");
    out.push_str("import { Contract, type ContractRunner, type ContractTransactionResponse, type EventFilter } from 'ethers';\n");
    out.push_str(&format!(
        "import {{ {}Abi }} from './{}.abi.js';\n\n",
        ir.name, ir.name
    ));

    // Interface — standalone (not extending Contract) to avoid index signature
    // conflicts with ethers v6's BaseContractMethod constraint.
    out.push_str(&format!("export interface {}Contract {{\n", ir.name));

    // Count overloaded function names.
    // Use &str keys to avoid cloning function names into the maps.
    let mut name_counts: HashMap<&str, u32> = HashMap::new();
    for f in &ir.functions {
        *name_counts.entry(f.name.as_str()).or_insert(0) += 1;
    }

    for f in &ir.functions {
        let count = *name_counts.get(f.name.as_str()).unwrap_or(&1);
        let overloaded;
        let method_name: &str = if count > 1 {
            overloaded = format!("{}{}", f.name, overload_suffix(&f.inputs));
            &overloaded
        } else {
            &f.name
        };
        out.push_str(&render_function_sig(method_name, f));
    }

    // Event filters
    if !ir.events.is_empty() {
        out.push_str("  filters: {\n");
        for event in &ir.events {
            out.push_str(&render_event_filter(event));
        }
        out.push_str("  };\n");
    }

    out.push_str("}\n\n");

    // connect factory function
    out.push_str(&format!(
        "export function connect{}(address: string, runner: ContractRunner): {}Contract {{\n",
        ir.name, ir.name
    ));
    out.push_str(&format!(
        "  return new Contract(address, {}Abi, runner) as unknown as {}Contract;\n",
        ir.name, ir.name
    ));
    out.push_str("}\n");

    out
}

fn render_function_sig(method_name: &str, f: &AbiFunction) -> String {
    let params = f
        .inputs
        .iter()
        .enumerate()
        .map(|(i, p)| {
            format!(
                "{}: {}",
                safe_param_name(&p.name, i),
                sol_type_to_ts(&p.ty, TARGET)
            )
        })
        .collect::<Vec<_>>()
        .join(", ");

    let return_type = match f.state_mutability {
        StateMutability::Pure | StateMutability::View => {
            let out_types: Vec<String> = f
                .outputs
                .iter()
                .map(|p| sol_type_to_ts(&p.ty, TARGET))
                .collect();
            match out_types.len() {
                0 => "Promise<void>".into(),
                1 => format!("Promise<{}>", out_types[0]),
                _ => {
                    let all_named = f.outputs.iter().all(|p| !p.name.is_empty());
                    if all_named {
                        let fields: Vec<String> = f
                            .outputs
                            .iter()
                            .enumerate()
                            .map(|(i, p)| {
                                format!(
                                    "{}: {}",
                                    safe_param_name(&p.name, i),
                                    sol_type_to_ts(&p.ty, TARGET)
                                )
                            })
                            .collect();
                        format!("Promise<{{ {} }}>", fields.join("; "))
                    } else {
                        format!("Promise<[{}]>", out_types.join(", "))
                    }
                }
            }
        }
        _ => "Promise<ContractTransactionResponse>".into(),
    };

    format!("  {}({}): {};\n", method_name, params, return_type)
}

fn render_event_filter(event: &AbiEvent) -> String {
    let params = event
        .inputs
        .iter()
        .enumerate()
        .filter(|(_, p)| p.indexed)
        .map(|(i, p)| {
            format!(
                "{}?: {} | null",
                safe_param_name(&p.name, i),
                sol_type_to_ts(&p.ty, TARGET)
            )
        })
        .collect::<Vec<_>>()
        .join(", ");

    format!("    {}({}): EventFilter;\n", event.name, params)
}

