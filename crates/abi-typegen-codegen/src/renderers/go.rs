use abi_typegen_core::types::{ContractIr, SolType, TupleComponent};
use heck::ToUpperCamelCase;
use std::collections::HashMap;

/// Generates `<ContractName>.go` — a Go source file with typed structs and ABI
/// compatible with go-ethereum's `abigen` style.
pub fn render_go_file(ir: &ContractIr) -> String {
    let mut out = String::new();

    // Header
    out.push_str("// Code generated by abi-typegen. DO NOT EDIT.\n");
    out.push_str("package contracts\n\n");

    // Collect which imports are needed
    let needs_big = ir_uses_big(ir);
    let needs_common = ir_uses_common(ir);

    if needs_big || needs_common {
        out.push_str("import (\n");
        if needs_big {
            out.push_str("\t\"math/big\"\n");
        }
        if needs_big && needs_common {
            out.push('\n');
        }
        if needs_common {
            out.push_str("\t\"github.com/ethereum/go-ethereum/common\"\n");
        }
        out.push_str(")\n\n");
    }

    // ABI const
    let abi_json = serde_json::to_string(&ir.raw_abi).unwrap_or_default();
    // Escape backticks in the JSON (Go raw strings cannot contain backticks)
    // so we use a regular quoted string instead if backticks are present.
    if abi_json.contains('`') {
        let escaped = abi_json.replace('\\', "\\\\").replace('"', "\\\"");
        out.push_str(&format!(
            "// {}ABI is the ABI of the {} contract.\nconst {}ABI = \"{}\"\n\n",
            ir.name, ir.name, ir.name, escaped
        ));
    } else {
        out.push_str(&format!(
            "// {}ABI is the ABI of the {} contract.\nconst {}ABI = `{}`\n\n",
            ir.name, ir.name, ir.name, abi_json
        ));
    }

    // Count function name occurrences for overload detection
    let mut name_counts: HashMap<&str, u32> = HashMap::new();
    for f in &ir.functions {
        *name_counts.entry(f.name.as_str()).or_insert(0) += 1;
    }

    // Function param structs
    for f in &ir.functions {
        if f.inputs.is_empty() {
            continue;
        }
        let count = *name_counts.get(f.name.as_str()).unwrap_or(&1);
        let suffix = if count > 1 {
            overload_suffix(&f.inputs)
        } else {
            String::new()
        };
        let struct_name = format!(
            "{}{}{}Params",
            ir.name,
            f.name.to_upper_camel_case(),
            suffix
        );

        // NatSpec doc comment
        if let Some(ref natspec) = f.natspec {
            if let Some(ref notice) = natspec.notice {
                out.push_str(&format!(
                    "// {} holds the parameters for the {} function.\n// {}\n",
                    struct_name, f.name, notice
                ));
            } else {
                out.push_str(&format!(
                    "// {} holds the parameters for the {} function.\n",
                    struct_name, f.name
                ));
            }
        } else {
            out.push_str(&format!(
                "// {} holds the parameters for the {} function.\n",
                struct_name, f.name
            ));
        }

        out.push_str(&format!("type {} struct {{\n", struct_name));
        for (i, param) in f.inputs.iter().enumerate() {
            let field_name = safe_param_name(&param.name, i).to_upper_camel_case();
            let go_type = sol_type_to_go(&param.ty);
            out.push_str(&format!("\t{} {}\n", field_name, go_type));
        }
        out.push_str("}\n\n");
    }

    // Event structs
    for event in &ir.events {
        let struct_name = format!("{}{}Event", ir.name, event.name);

        if let Some(ref natspec) = event.natspec {
            if let Some(ref notice) = natspec.notice {
                out.push_str(&format!(
                    "// {} represents the {} event.\n// {}\n",
                    struct_name, event.name, notice
                ));
            } else {
                out.push_str(&format!(
                    "// {} represents the {} event.\n",
                    struct_name, event.name
                ));
            }
        } else {
            out.push_str(&format!(
                "// {} represents the {} event.\n",
                struct_name, event.name
            ));
        }

        out.push_str(&format!("type {} struct {{\n", struct_name));
        for (i, param) in event.inputs.iter().enumerate() {
            let field_name = safe_param_name(&param.name, i).to_upper_camel_case();
            let go_type = sol_type_to_go(&param.ty);
            out.push_str(&format!("\t{} {}\n", field_name, go_type));
        }
        out.push_str("}\n\n");
    }

    // Error structs
    for error in &ir.errors {
        if error.inputs.is_empty() {
            continue;
        }
        let struct_name = format!("{}{}Error", ir.name, error.name);

        if let Some(ref natspec) = error.natspec {
            if let Some(ref notice) = natspec.notice {
                out.push_str(&format!(
                    "// {} represents the {} error.\n// {}\n",
                    struct_name, error.name, notice
                ));
            } else {
                out.push_str(&format!(
                    "// {} represents the {} error.\n",
                    struct_name, error.name
                ));
            }
        } else {
            out.push_str(&format!(
                "// {} represents the {} error.\n",
                struct_name, error.name
            ));
        }

        out.push_str(&format!("type {} struct {{\n", struct_name));
        for (i, param) in error.inputs.iter().enumerate() {
            let field_name = safe_param_name(&param.name, i).to_upper_camel_case();
            let go_type = sol_type_to_go(&param.ty);
            out.push_str(&format!("\t{} {}\n", field_name, go_type));
        }
        out.push_str("}\n\n");
    }

    // Remove trailing extra newline
    while out.ends_with("\n\n") {
        out.pop();
    }
    // Ensure file ends with exactly one newline
    if !out.ends_with('\n') {
        out.push('\n');
    }

    out
}

/// Converts a Solidity type to its Go type representation.
fn sol_type_to_go(ty: &SolType) -> String {
    match ty {
        SolType::Bool => "bool".into(),
        SolType::StringType => "string".into(),
        SolType::Address => "common.Address".into(),
        SolType::Bytes => "[]byte".into(),
        SolType::BytesN(n) => format!("[{}]byte", n),

        SolType::Uint(bits) => {
            if *bits <= 8 {
                "uint8".into()
            } else if *bits <= 16 {
                "uint16".into()
            } else if *bits <= 32 {
                "uint32".into()
            } else if *bits <= 64 {
                "uint64".into()
            } else {
                "*big.Int".into()
            }
        }

        SolType::Int(bits) => {
            if *bits <= 8 {
                "int8".into()
            } else if *bits <= 16 {
                "int16".into()
            } else if *bits <= 32 {
                "int32".into()
            } else if *bits <= 64 {
                "int64".into()
            } else {
                "*big.Int".into()
            }
        }

        SolType::Array(inner) => {
            let inner_go = sol_type_to_go(inner);
            format!("[]{}", inner_go)
        }

        SolType::FixedArray(inner, n) => {
            let inner_go = sol_type_to_go(inner);
            format!("[{}]{}", n, inner_go)
        }

        SolType::Tuple(components) => render_inline_struct(components),
    }
}

/// Renders an inline Go struct for tuple types.
fn render_inline_struct(components: &[TupleComponent]) -> String {
    if components.is_empty() {
        return "struct{}".into();
    }
    let mut s = String::from("struct {\n");
    for (i, c) in components.iter().enumerate() {
        let field_name = safe_param_name(&c.name, i).to_upper_camel_case();
        let go_type = sol_type_to_go(&c.ty);
        s.push_str(&format!("\t\t{} {}\n", field_name, go_type));
    }
    s.push('\t');
    s.push('}');
    s
}

/// Returns `true` if any type in the contract IR uses `*big.Int`.
fn ir_uses_big(ir: &ContractIr) -> bool {
    let check_params = |params: &[abi_typegen_core::types::AbiParam]| -> bool {
        params.iter().any(|p| type_uses_big(&p.ty))
    };
    let check_event_params = |params: &[abi_typegen_core::types::AbiEventParam]| -> bool {
        params.iter().any(|p| type_uses_big(&p.ty))
    };

    ir.functions
        .iter()
        .any(|f| check_params(&f.inputs) || check_params(&f.outputs))
        || ir.events.iter().any(|e| check_event_params(&e.inputs))
        || ir.errors.iter().any(|e| check_params(&e.inputs))
}

/// Returns `true` if any type in the contract IR uses `common.Address`.
fn ir_uses_common(ir: &ContractIr) -> bool {
    let check_params = |params: &[abi_typegen_core::types::AbiParam]| -> bool {
        params.iter().any(|p| type_uses_common(&p.ty))
    };
    let check_event_params = |params: &[abi_typegen_core::types::AbiEventParam]| -> bool {
        params.iter().any(|p| type_uses_common(&p.ty))
    };

    ir.functions
        .iter()
        .any(|f| check_params(&f.inputs) || check_params(&f.outputs))
        || ir.events.iter().any(|e| check_event_params(&e.inputs))
        || ir.errors.iter().any(|e| check_params(&e.inputs))
}

/// Returns `true` if the given Solidity type maps to a Go type that uses `*big.Int`.
fn type_uses_big(ty: &SolType) -> bool {
    match ty {
        SolType::Uint(bits) | SolType::Int(bits) => *bits > 64,
        SolType::Array(inner) | SolType::FixedArray(inner, _) => type_uses_big(inner),
        SolType::Tuple(components) => components.iter().any(|c| type_uses_big(&c.ty)),
        _ => false,
    }
}

/// Returns `true` if the given Solidity type maps to a Go type that uses `common.Address`.
fn type_uses_common(ty: &SolType) -> bool {
    match ty {
        SolType::Address => true,
        SolType::Array(inner) | SolType::FixedArray(inner, _) => type_uses_common(inner),
        SolType::Tuple(components) => components.iter().any(|c| type_uses_common(&c.ty)),
        _ => false,
    }
}

