    fn make_ir(
        name: &str,
        functions: Vec<AbiFunction>,
        events: Vec<AbiEvent>,
        errors: Vec<AbiError>,
    ) -> ContractIr {
        ContractIr {
            name: name.to_string(),
            constructor: None,
            functions,
            events,
            errors,
            has_fallback: false,
            has_receive: false,
            natspec: None,
            raw_abi: serde_json::Value::Array(vec![]),
        }
    }

