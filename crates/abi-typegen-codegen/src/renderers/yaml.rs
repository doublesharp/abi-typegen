//! YAML renderer for Solidity ABI contracts.
//!
//! Generates a `<ContractName>.yaml` file containing a structured, human-readable
//! YAML description of the contract's ABI — constructor, functions, events, and errors.

use abi_typegen_core::types::{
    AbiError, AbiEvent, AbiEventParam, AbiFunction, AbiParam, ContractIr, SolType, StateMutability,
    TupleComponent,
};

/// Generates `<ContractName>.yaml` -- a YAML ABI description of the contract.
pub fn render_yaml_file(ir: &ContractIr) -> String {
    let mut out = String::new();

    out.push_str("# Auto-generated by abi-typegen. Do not edit manually.\n");
    out.push_str(&format!("name: \"{}\"\n", yaml_escape(&ir.name)));

    // Constructor
    if let Some(ref ctor) = ir.constructor {
        out.push_str("constructor:\n");
        out.push_str(&format!(
            "  stateMutability: \"{}\"\n",
            mutability_str(&ctor.state_mutability)
        ));
        if ctor.inputs.is_empty() {
            out.push_str("  inputs: []\n");
        } else {
            out.push_str("  inputs:\n");
            for p in &ctor.inputs {
                render_param(&mut out, p, 4);
            }
        }
    }

    // Functions
    if ir.functions.is_empty() {
        out.push_str("functions: []\n");
    } else {
        out.push_str("functions:\n");
        for f in &ir.functions {
            render_function(&mut out, f);
        }
    }

    // Events
    if ir.events.is_empty() {
        out.push_str("events: []\n");
    } else {
        out.push_str("events:\n");
        for e in &ir.events {
            render_event(&mut out, e);
        }
    }

    // Errors
    if ir.errors.is_empty() {
        out.push_str("errors: []\n");
    } else {
        out.push_str("errors:\n");
        for e in &ir.errors {
            render_error(&mut out, e);
        }
    }

    out
}

/// Renders a single ABI function as a YAML list item.
fn render_function(out: &mut String, f: &AbiFunction) {
    out.push_str(&format!("  - name: \"{}\"\n", yaml_escape(&f.name)));
    out.push_str(&format!(
        "    stateMutability: \"{}\"\n",
        mutability_str(&f.state_mutability)
    ));

    // NatSpec
    if let Some(ref ns) = f.natspec {
        if let Some(ref notice) = ns.notice {
            out.push_str(&format!("    notice: \"{}\"\n", yaml_escape(notice.trim())));
        }
    }

    // Inputs
    if f.inputs.is_empty() {
        out.push_str("    inputs: []\n");
    } else {
        out.push_str("    inputs:\n");
        for p in &f.inputs {
            render_param(out, p, 6);
        }
    }

    // Outputs
    if f.outputs.is_empty() {
        out.push_str("    outputs: []\n");
    } else {
        out.push_str("    outputs:\n");
        for p in &f.outputs {
            render_param(out, p, 6);
        }
    }
}

/// Renders a single ABI event as a YAML list item.
fn render_event(out: &mut String, e: &AbiEvent) {
    out.push_str(&format!("  - name: \"{}\"\n", yaml_escape(&e.name)));
    if e.anonymous {
        out.push_str("    anonymous: true\n");
    }

    // NatSpec
    if let Some(ref ns) = e.natspec {
        if let Some(ref notice) = ns.notice {
            out.push_str(&format!("    notice: \"{}\"\n", yaml_escape(notice.trim())));
        }
    }

    if e.inputs.is_empty() {
        out.push_str("    inputs: []\n");
    } else {
        out.push_str("    inputs:\n");
        for p in &e.inputs {
            render_event_param(out, p, 6);
        }
    }
}

/// Renders a single ABI error as a YAML list item.
fn render_error(out: &mut String, e: &AbiError) {
    out.push_str(&format!("  - name: \"{}\"\n", yaml_escape(&e.name)));

    // NatSpec
    if let Some(ref ns) = e.natspec {
        if let Some(ref notice) = ns.notice {
            out.push_str(&format!("    notice: \"{}\"\n", yaml_escape(notice.trim())));
        }
    }

    if e.inputs.is_empty() {
        out.push_str("    inputs: []\n");
    } else {
        out.push_str("    inputs:\n");
        for p in &e.inputs {
            render_param(out, p, 6);
        }
    }
}

/// Renders a function/error parameter at the given indentation level.
fn render_param(out: &mut String, p: &AbiParam, indent: usize) {
    let pad = " ".repeat(indent);
    let name_str = if p.name.is_empty() {
        String::new()
    } else {
        format!("name: \"{}\", ", yaml_escape(&p.name))
    };
    match &p.ty {
        SolType::Tuple(components) => {
            out.push_str(&format!("{}- {}type: tuple\n", pad, name_str));
            out.push_str(&format!("{}  components:\n", pad));
            for c in components {
                render_tuple_component(out, c, indent + 4);
            }
        }
        ty => {
            out.push_str(&format!(
                "{}- {}type: \"{}\"\n",
                pad,
                name_str,
                sol_type_to_yaml(ty)
            ));
        }
    }
}

/// Renders an event parameter at the given indentation level.
fn render_event_param(out: &mut String, p: &AbiEventParam, indent: usize) {
    let pad = " ".repeat(indent);
    let name_str = if p.name.is_empty() {
        String::new()
    } else {
        format!("name: \"{}\", ", yaml_escape(&p.name))
    };
    let indexed_str = if p.indexed { ", indexed: true" } else { "" };
    match &p.ty {
        SolType::Tuple(components) => {
            out.push_str(&format!(
                "{}- {}type: tuple{}\n",
                pad, name_str, indexed_str
            ));
            out.push_str(&format!("{}  components:\n", pad));
            for c in components {
                render_tuple_component(out, c, indent + 4);
            }
        }
        ty => {
            out.push_str(&format!(
                "{}- {}type: \"{}\"{}\n",
                pad,
                name_str,
                sol_type_to_yaml(ty),
                indexed_str
            ));
        }
    }
}

/// Renders a tuple component at the given indentation level.
fn render_tuple_component(out: &mut String, c: &TupleComponent, indent: usize) {
    let pad = " ".repeat(indent);
    let name_str = if c.name.is_empty() {
        String::new()
    } else {
        format!("name: \"{}\", ", yaml_escape(&c.name))
    };
    match &c.ty {
        SolType::Tuple(inner_components) => {
            out.push_str(&format!("{}- {}type: tuple\n", pad, name_str));
            out.push_str(&format!("{}  components:\n", pad));
            for ic in inner_components {
                render_tuple_component(out, ic, indent + 4);
            }
        }
        ty => {
            out.push_str(&format!(
                "{}- {}type: \"{}\"\n",
                pad,
                name_str,
                sol_type_to_yaml(ty)
            ));
        }
    }
}

/// Converts a [`SolType`] to its YAML type string representation.
fn sol_type_to_yaml(ty: &SolType) -> String {
    match ty {
        SolType::Uint(bits) => format!("uint{}", bits),
        SolType::Int(bits) => format!("int{}", bits),
        SolType::Bool => "bool".into(),
        SolType::Address => "address".into(),
        SolType::Bytes => "bytes".into(),
        SolType::BytesN(n) => format!("bytes{}", n),
        SolType::StringType => "string".into(),
        SolType::Array(inner) => format!("{}[]", sol_type_to_yaml(inner)),
        SolType::FixedArray(inner, size) => format!("{}[{}]", sol_type_to_yaml(inner), size),
        SolType::Tuple(_) => "tuple".into(),
    }
}

/// Converts a [`StateMutability`] to its string representation.
fn mutability_str(m: &StateMutability) -> &'static str {
    match m {
        StateMutability::Pure => "pure",
        StateMutability::View => "view",
        StateMutability::NonPayable => "nonpayable",
        StateMutability::Payable => "payable",
    }
}

/// Escapes a string for use in a YAML double-quoted value.
fn yaml_escape(s: &str) -> String {
    s.replace('\\', "\\\\").replace('"', "\\\"")
}

