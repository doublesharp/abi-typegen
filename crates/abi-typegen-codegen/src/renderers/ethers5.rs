/// Maps a Solidity type to its ethers v5 TypeScript representation.
///
/// Key differences from the shared `sol_type_to_ts` (which targets ethers v6):
/// - `uint56`..`uint256` and `int56`..`int256` map to `BigNumber` (not `bigint`).
/// - `address` maps to `string`.
/// - `bytes` / `bytesN` map to `string`.
/// - Arrays are `T[]` (not `readonly T[]`).
/// - Fixed-size arrays are also `T[]` (not `readonly [T, T, ...]`).
/// - Tuple fields are rendered inline via recursion.
fn sol_type_to_ethers5(ty: &SolType) -> String {
    match ty {
        SolType::Bool => "boolean".into(),
        SolType::StringType => "string".into(),

        SolType::Uint(bits) | SolType::Int(bits) => {
            if *bits <= 48 {
                "number".into()
            } else {
                "BigNumber".into()
            }
        }

        SolType::Address => "string".into(),

        SolType::Bytes | SolType::BytesN(_) => "string".into(),

        SolType::Array(inner) => {
            let inner_ts = sol_type_to_ethers5(inner);
            format!("{}[]", inner_ts)
        }

        SolType::FixedArray(inner, _n) => {
            let inner_ts = sol_type_to_ethers5(inner);
            format!("{}[]", inner_ts)
        }

        SolType::Tuple(components) => {
            let fields: Vec<String> = components
                .iter()
                .enumerate()
                .map(|(i, c)| {
                    format!(
                        "{}: {}",
                        safe_param_name(&c.name, i),
                        sol_type_to_ethers5(&c.ty)
                    )
                })
                .collect();
            format!("{{ {} }}", fields.join("; "))
        }
    }
}

/// Maps a Solidity type to the ethers v5 *input* TypeScript type.
///
/// For integer types > 48 bits, returns `BigNumberish` instead of `BigNumber`,
/// since inputs should accept `string | number | BigNumber`.
fn sol_type_to_ethers5_input(ty: &SolType) -> String {
    match ty {
        SolType::Uint(bits) | SolType::Int(bits) => {
            if *bits <= 48 {
                "number".into()
            } else {
                "BigNumberish".into()
            }
        }
        // For all other types, the input type is the same as the output type.
        _ => sol_type_to_ethers5(ty),
    }
}

/// Generates `<ContractName>.ethers5.ts` -- a typed ethers v5 contract interface.
pub fn render_ethers5_file(ir: &ContractIr) -> String {
    let mut out = String::new();

    out.push_str("// Auto-generated by abi-typegen. Do not edit manually.\n");
    out.push_str("import { ethers, BigNumber, BigNumberish } from 'ethers';\n");
    out.push_str(&format!(
        "import {{ {}Abi }} from './{}.abi.js';\n\n",
        ir.name, ir.name
    ));

    // Interface -- extends ethers.Contract
    out.push_str(&format!(
        "export interface {}Contract extends ethers.Contract {{\n",
        ir.name
    ));

    // Count overloaded function names.
    let mut name_counts: HashMap<&str, u32> = HashMap::new();
    for f in &ir.functions {
        *name_counts.entry(f.name.as_str()).or_insert(0) += 1;
    }

    for f in &ir.functions {
        let count = *name_counts.get(f.name.as_str()).unwrap_or(&1);
        let overloaded;
        let method_name: &str = if count > 1 {
            overloaded = format!("{}{}", f.name, overload_suffix(&f.inputs));
            &overloaded
        } else {
            &f.name
        };
        out.push_str(&render_function_sig(method_name, f));
    }

    // Event filters
    if !ir.events.is_empty() {
        out.push_str("  filters: {\n");
        for event in &ir.events {
            out.push_str(&render_event_filter(event));
        }
        out.push_str("  };\n");
    }

    out.push_str("}\n\n");

    // connect factory function
    out.push_str(&format!(
        "export function connect{}(\n  address: string,\n  signerOrProvider: ethers.Signer | ethers.providers.Provider,\n): {}Contract {{\n",
        ir.name, ir.name
    ));
    out.push_str(&format!(
        "  return new ethers.Contract(address, {}Abi, signerOrProvider) as unknown as {}Contract;\n",
        ir.name, ir.name
    ));
    out.push_str("}\n");

    out
}

/// Renders a single function signature line for an ethers v5 typed interface.
fn render_function_sig(method_name: &str, f: &AbiFunction) -> String {
    let params = f
        .inputs
        .iter()
        .enumerate()
        .map(|(i, p)| {
            format!(
                "{}: {}",
                safe_param_name(&p.name, i),
                sol_type_to_ethers5_input(&p.ty)
            )
        })
        .collect::<Vec<_>>()
        .join(", ");

    let return_type = match f.state_mutability {
        StateMutability::Pure | StateMutability::View => {
            let out_types: Vec<String> = f
                .outputs
                .iter()
                .map(|p| sol_type_to_ethers5(&p.ty))
                .collect();
            match out_types.len() {
                0 => "Promise<void>".into(),
                1 => format!("Promise<{}>", out_types[0]),
                _ => {
                    let all_named = f.outputs.iter().all(|p| !p.name.is_empty());
                    if all_named {
                        let fields: Vec<String> = f
                            .outputs
                            .iter()
                            .enumerate()
                            .map(|(i, p)| {
                                format!(
                                    "{}: {}",
                                    safe_param_name(&p.name, i),
                                    sol_type_to_ethers5(&p.ty)
                                )
                            })
                            .collect();
                        format!("Promise<{{ {} }}>", fields.join("; "))
                    } else {
                        format!("Promise<[{}]>", out_types.join(", "))
                    }
                }
            }
        }
        _ => "Promise<ethers.ContractTransaction>".into(),
    };

    format!("  {}({}): {};\n", method_name, params, return_type)
}

/// Renders a single event filter method line for ethers v5.
fn render_event_filter(event: &AbiEvent) -> String {
    let params = event
        .inputs
        .iter()
        .enumerate()
        .filter(|(_, p)| p.indexed)
        .map(|(i, p)| {
            format!(
                "{}?: {} | null",
                safe_param_name(&p.name, i),
                sol_type_to_ethers5(&p.ty)
            )
        })
        .collect::<Vec<_>>()
        .join(", ");

    format!("    {}({}): ethers.EventFilter;\n", event.name, params)
}

