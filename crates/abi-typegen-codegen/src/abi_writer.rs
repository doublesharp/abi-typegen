use abi_typegen_core::types::ContractIr;

/// Generates `<ContractName>.abi.ts` — an `as const` ABI export.
/// The raw ABI JSON is pretty-printed and emitted verbatim.
pub fn render_abi_file(ir: &ContractIr) -> String {
    let abi_json = serde_json::to_string_pretty(&ir.raw_abi).expect("raw_abi is always valid JSON");

    // Indent every line after the first by 2 extra spaces.
    // Pre-allocate with headroom for the added indentation characters.
    let mut indented = String::with_capacity(abi_json.len() + abi_json.lines().count() * 2);
    for (i, line) in abi_json.lines().enumerate() {
        if i > 0 {
            indented.push('\n');
            indented.push_str("  ");
        }
        indented.push_str(line);
    }

    format!(
        "// Auto-generated by abi-typegen. Do not edit manually.\n\
         export const {}Abi = {} as const;\n",
        ir.name, indented
    )
}

#[cfg(test)]
mod tests {
    use super::*;
    use abi_typegen_core::parser::parse_artifact;

    fn erc20() -> ContractIr {
        let json = std::fs::read_to_string(
            std::path::Path::new(env!("CARGO_MANIFEST_DIR"))
                .join("../../tests/fixtures/erc20.json"),
        )
        .unwrap();
        parse_artifact("ERC20", &json).unwrap()
    }

    fn minimal() -> ContractIr {
        let json = std::fs::read_to_string(
            std::path::Path::new(env!("CARGO_MANIFEST_DIR"))
                .join("../../tests/fixtures/minimal.json"),
        )
        .unwrap();
        parse_artifact("Minimal", &json).unwrap()
    }

    #[test]
    fn contains_header_comment() {
        let out = render_abi_file(&erc20());
        assert!(out.starts_with("// Auto-generated by abi-typegen."));
    }

    #[test]
    fn exports_const_with_as_const() {
        let out = render_abi_file(&erc20());
        assert!(out.contains("export const ERC20Abi ="));
        assert!(out.contains("] as const;"));
    }

    #[test]
    fn includes_function_name() {
        let out = render_abi_file(&erc20());
        assert!(out.contains("\"balanceOf\""));
        assert!(out.contains("\"transfer\""));
    }

    #[test]
    fn includes_event_name() {
        let out = render_abi_file(&erc20());
        assert!(out.contains("\"Transfer\""));
    }

    #[test]
    fn minimal_abi_valid() {
        let out = render_abi_file(&minimal());
        assert!(out.contains("export const MinimalAbi ="));
        assert!(out.contains("] as const;"));
    }
}
